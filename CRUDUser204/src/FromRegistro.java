    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // 1. Guardamos en variables lo que escribe el susuario 
        String nom = txtNombre.getText();
        String cor = txtCorreo.getText();
        String con = txtContrasena.getText();
        
        //2. Validar que no existan vacios 
        if(nom.isEmpty() || cor.isEmpty() || con.isEmpty()){
            JOptionPane.showMessageDialog( this, "Campos Obligatorios","Error", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        
        //3. Insertar Datos
        UserCRUD crud = new UserCRUD();
        boolean status = crud.InsertarUsuario(nom, cor, con);
        
        //4. Avisamos status del INSERT 
        if(status){
            JOptionPane.showMessageDialog( this, "Usuaro Guardado","Exito", JOptionPane.INFORMATION_MESSAGE);
        }else{
            JOptionPane.showMessageDialog( this, "No se Guardo","Error", JOptionPane.INFORMATION_MESSAGE);
        }
        
        
    }//GEN-LAST:event_btnGuardarActionPerformed

