    private void btnTodosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTodosActionPerformed
         // TODO add your handling code here:
        
        ResultSet todos = crud.obtenerTodos();
        DefaultTableModel modeloT = (DefaultTableModel) jTable1.getModel();
        modeloT.setRowCount(0);
        
        try{
          while(todos.next()){
              modeloT.addRow(new Object[]{todos.getInt("idusuarios"), todos.getString("Nombre"),todos.getString("Correo"),todos.getString("Contrasena")});
          }  
        }
        catch(SQLException x){
            System.out.println("Error al llenar la tabla "+x.getMessage());
        }
                
    }//GEN-LAST:event_btnTodosActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // 1. Obtener el valor del txt 
        String idtext= txtId.getText();
        
        //2. Validar txt vacio 
        if(idtext.isEmpty()){
            JOptionPane.showMessageDialog(this, "El id es obligatorio","Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

         //3. Busqueda, llenado de la tabla 
        try{
            int id= Integer.parseInt(idtext);
            ResultSet rs= crud.buscarPorID(id);
            DefaultTableModel modelo= (DefaultTableModel) jTable1.getModel();
            modelo.setRowCount(0);

            if(rs.next()){
                modelo.addRow(new Object[]{rs.getInt("idusuarios"),rs.getString("Nombre"),rs.getString("Correo"),rs.getString("Contrasena")});
        }

        }
        catch(SQLException e){
                System.out.println("Error al llenar la tabla: "+e.getMessage());
        }
        
        
    }//GEN-LAST:event_btnBuscarActionPerformed

